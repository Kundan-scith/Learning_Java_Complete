public class loops {
    public static void main(String[] args){
        //loops
        for(int i= 1; i<=10; i++){
            System.out.print(i+" ");
        }

        for(int j=10;j>=1; j--){
            System.out.print(j+ " ");
        }

        int k = 1;
        while(k<=10){
            System.out.println(k);
            k++;

        }

    }
    
}
