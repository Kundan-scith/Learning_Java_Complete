public class constants {
    public static void main(String[] args){
        //constants

        int age = 20;
        age = 21;
        age = 22;

        final float PI = 3.14F;
        //after adding final in front of primitive data type we cannot change the value maling it a constant
        System.out.println(age);
        System.out.println(PI);

    }
    
}
